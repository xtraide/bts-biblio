<h3 class="cnil">Qu'est-ce qu'un "cookie" ?</h3><br>
<p class="espace">Un "cookie" est une suite d'informations, généralement de petite taille et
 identifié par un nom, qui peut être transmis à votre navigateur 
 par un site web sur lequel vous vous connectez. Votre navigateur web 
 le conservera pendant une certaine durée, et le renverra au serveur web
  chaque fois que vous vous y re-connecterez. Les cookies ont de multiples usages 
  : ils peuvent servir à mémoriser votre identifiant client auprès d'un site marchand, 
  le contenu courant de votre panier d'achat, un identifiant permettant de tracer votre 
